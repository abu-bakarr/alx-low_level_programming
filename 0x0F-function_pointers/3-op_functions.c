#include "3-calc.h"

/**
 * op_add - entry point
 * @a: entry argument
 * @b: entry argument
 * Return: always success
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - entry point
 * @a: entry argument
 * @b: entry argument
 * Return: always success
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - entry point
 * @a: entry argument
 * @b: entry argument
 * Return: always success
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - entry point
 * @a: entry argument
 * @b: entry argument
 * Return: always success
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - entry point
 * @a: entry argument
 * @b: entry argument
 * Return: always success
 */
int op_mod(int a, int b)
{
	return (a % b);
}
